package com.cic.textengine.repository.datanode.type;


public class DataNodeIDFStatus {
	String dataNodeKey = null;
	int year, month, IDFIdx, MAXItemCount, itemCount;
	String siteID, forumID;
	public String getDataNodeKey() {
		return dataNodeKey;
	}
	public void setDataNodeKey(String dataNodeKey) {
		this.dataNodeKey = dataNodeKey;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getIDFIdx() {
		return IDFIdx;
	}
	public void setIDFIdx(int idx) {
		IDFIdx = idx;
	}
	public int getMAXItemCount() {
		return MAXItemCount;
	}
	public void setMAXItemCount(int itemCount) {
		MAXItemCount = itemCount;
	}
	public int getItemCount() {
		return itemCount;
	}
	public void setItemCount(int itemCount) {
		this.itemCount = itemCount;
	}
	public String getSiteID() {
		return siteID;
	}
	public void setSiteID(String siteID) {
		this.siteID = siteID;
	}
	public String getForumID() {
		return forumID;
	}
	public void setForumID(String forumID) {
		this.forumID = forumID;
	}
}
